import UIKit

extension UIColor /** +Theme */ {
    class var tint: UIColor {
        return UIColor(named: "tint")!
    }
    class var tintGreen: UIColor {
        return UIColor(named: "tintGreen")!
    }
    class var tintRed: UIColor {
        return UIColor(named: "tintRed")!
    }
    class var tintOrange: UIColor {
        return UIColor(named: "tintOrange")!
    }
    class var secondary: UIColor {
        return UIColor(named: "secondary")!
    }
    class var primary: UIColor {
        return UIColor(named: "primary")!
    }
    class var minor: UIColor {
        return UIColor(named: "minor")!
    }
}
